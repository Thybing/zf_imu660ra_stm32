// 函数简介     获取 IMU660RA 加速度计物理量数据
// 参数说明     *measurement_data       IMU660RA 的测量数据缓冲指针
// 返回参数     range                   IMU660RA 加速度量程 详见 zf_device_imu660ra.h 中 imu660ra_acc_range_enum 定义
// 参数说明     *physical_data          IMU660RA 的物理量数据缓冲指针
// 返回参数     imu660ra_state_enum     IMU660RA 状态码 详见 zf_device_imu660ra.h 中 imu660ra_state_enum 定义
// 使用示例     imu660ra_get_physical_acc(imu660_data_buffer, range, physical_data);
// 备注信息     根据量程进行物理量换算
//              加速度计量程 ±2  g   获取到的加速度计数据除以 16384  可以转化为带物理单位的数据 (g 代表重力加速度
//              物理学名词 一般情况下 g 取 9.8 m/s^2 为标准值) 加速度计量程 ±4  g   获取到的加速度计数据除以 8192
//              可以转化为带物理单位的数据 (g 代表重力加速度 物理学名词 一般情况下 g 取 9.8 m/s^2 为标准值) 加速度计量程
//              ±8  g   获取到的加速度计数据除以 4096   可以转化为带物理单位的数据 (g 代表重力加速度 物理学名词
//              一般情况下 g 取 9.8 m/s^2 为标准值) 加速度计量程 ±16 g   获取到的加速度计数据除以 2048
//              可以转化为带物理单位的数据 (g 代表重力加速度 物理学名词 一般情况下 g 取 9.8 m/s^2 为标准值)
//-------------------------------------------------------------------------------------------------------------------
imu660ra_state_enum imu660ra_get_physical_acc(imu660ra_measurement_data_struct *measurement_data,
                                              imu660ra_acc_range_enum range,
                                              imu660ra_physical_data_struct *physical_data) {
    imu660ra_state_enum return_state = IMU660RA_STATE_NO_ERROR;
    do {
        if (imu_assert(NULL != measurement_data,    // 缓冲区指针 不能为空
                       "*measurement_data error.",  // 输出预设报错信息
                       __FILE__, __LINE__)) {
            return_state = IMU660RA_STATE_DATA_BUFFER_ERROR;
            break;
        }
        if (imu_assert(IMU660RA_ACC_RANGE_SGN_2G <= range && IMU660RA_ACC_RANGE_SGN_16G >= range,
                       "range error.",  // 输出预设报错信息
                       __FILE__, __LINE__)) {
            return_state = IMU660RA_STATE_SET_ACC_RANGE_ERROR;
            break;
        }
        if (imu_assert(NULL != physical_data,    // 缓冲区指针 不能为空
                       "*physical_data error.",  // 输出预设报错信息
                       __FILE__, __LINE__)) {
            return_state = IMU660RA_STATE_DATA_BUFFER_ERROR;
            break;
        }
        const float transition_factor[] = {16384, 8192, 4096, 2048};
        physical_data->x = (float)measurement_data->x / transition_factor[range - IMU660RA_ACC_RANGE_SGN_2G];
        physical_data->y = (float)measurement_data->y / transition_factor[range - IMU660RA_ACC_RANGE_SGN_2G];
        physical_data->z = (float)measurement_data->z / transition_factor[range - IMU660RA_ACC_RANGE_SGN_2G];
    } while (0);
    return return_state;
}

