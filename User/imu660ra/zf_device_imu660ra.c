// 函数简介     IMU660RA 配置模块工作参数 拓展接口
// 参数说明     *interface              IMU660RA 的通信接口对象指针
// 参数说明     item                    IMU660RA 配置参照 zf_device_imu660ra.h 中 imu660ra_config_item_enum 枚举体定义
// 返回参数     imu660ra_state_enum     IMU660RA 状态码 详见 zf_device_imu660ra.h 中 imu660ra_state_enum 定义
// 使用示例     imu660ra_expansion_set_config(interface, item);
// 备注信息     本接口为传感器框架接口 并不直接对用户开放
//-------------------------------------------------------------------------------------------------------------------
static imu660ra_state_enum imu660ra_expansion_set_config(imu_interface_obj_struct *interface,
                                                         imu660ra_config_item_enum item) {
    imu660ra_state_enum return_state = IMU660RA_STATE_NO_ERROR;
    do {
        switch ((item >> IMU660RA_CONFIG_TYPE_OFFSET) & IMU660RA_CONFIG_TYPE_MASK) {
            case 0x00: {
                if (interface->assert(IMU660RA_ACC_OUTPUT_RATE_64_DIV <= (item & 0x0F) &&
                                          IMU660RA_ACC_OUTPUT_RATE_32_MUL >= (item & 0x0F),
                                      "item set error.",  // 输出预设报错信息
                                      __FILE__, __LINE__)) {
                    return_state = IMU660RA_STATE_SET_ACC_OUTPUT_RATE_ERROR;
                    break;
                }
                interface->write_8bit_register(IMU660RA_DEV_ADDR, IMU660RA_ACC_CONF, 0xA0 | ((item & 0x0F) - 1));
            } break;
            case 0x01: {
                if (interface->assert(
                        IMU660RA_ACC_RANGE_SGN_2G <= (item & 0x0F) && IMU660RA_ACC_RANGE_SGN_16G >= (item & 0x0F),
                        "item set error.",  // 输出预设报错信息
                        __FILE__, __LINE__)) {
                    return_state = IMU660RA_STATE_SET_ACC_RANGE_ERROR;
                    break;
                }
                interface->write_8bit_register(IMU660RA_DEV_ADDR, IMU660RA_ACC_RANGE, (item & 0x0F) - 2);
            } break;
            case 0x02: {
                if (interface->assert(IMU660RA_GYRO_OUTPUT_RATE_2_DIV <= (item & 0x0F) &&
                                          IMU660RA_GYRO_OUTPUT_RATE_64_MUL >= (item & 0x0F),
                                      "item set error.",  // 输出预设报错信息
                                      __FILE__, __LINE__)) {
                    return_state = IMU660RA_STATE_SET_GYRO_OUTPUT_RATE_ERROR;
                    break;
                }
                interface->write_8bit_register(IMU660RA_DEV_ADDR, IMU660RA_GYR_CONF, 0xA0 | ((item & 0x0F) - 1));
            } break;
            case 0x03: {
                if (interface->assert(IMU660RA_GYRO_RANGE_SGN_125DPS <= (item & 0x0F) &&
                                          IMU660RA_GYRO_RANGE_SGN_2000DPS >= (item & 0x0F),
                                      "item set error.",  // 输出预设报错信息
                                      __FILE__, __LINE__)) {
                    return_state = IMU660RA_STATE_SET_GYRO_RANGE_ERROR;
                    break;
                }
                interface->write_8bit_register(IMU660RA_DEV_ADDR, IMU660RA_GYR_RANGE, 6 - (item & 0x0F));
            } break;
            default: {
                if (interface->assert(0,
                                      "item set error.",  // 输出预设报错信息
                                      __FILE__, __LINE__)) {
                    return_state = IMU660RA_STATE_SET_CONFIG_ERROR;
                    break;
                }
            } break;
        }
    } while (0);
    return return_state;
}

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     IMU660RA 检测 ID 是否匹配 拓展接口
// 参数说明     *interface              IMU660RA 的通信接口对象指针
// 返回参数     imu660ra_state_enum     IMU660RA 状态码 详见 zf_device_imu660ra.h 中 imu660ra_state_enum 定义
// 使用示例     imu660ra_expansion_ckeck_id(interface);
// 备注信息     本接口为传感器框架接口 并不直接对用户开放
//-------------------------------------------------------------------------------------------------------------------
static imu660ra_state_enum imu660ra_expansion_ckeck_id(imu_interface_obj_struct *interface) {
    imu660ra_state_enum return_state = IMU660RA_STATE_NO_ERROR;
    uint8_t dat[2];
    uint16_t timeout_count = 0;
    do {
        if (IMU660RA_TIMEOUT_COUNT < timeout_count++) {
            return_state = IMU660RA_STATE_ID_CHECK_ERROR;
            break;
        }
        if (IMU_INTERFACE_SPI == interface->interface_type) {
            interface->read_8bit_registers(IMU660RA_DEV_ADDR, IMU660RA_CHIP_ID | IMU660RA_SPI_R, dat, 2);
        } else {
            dat[1] = interface->read_8bit_register(IMU660RA_DEV_ADDR, IMU660RA_CHIP_ID);
        }
        interface->delay_ms(1);
    } while (0x24 != dat[1]);
    return return_state;
}

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     初始化 IMU660RA 拓展接口
// 参数说明     *interface              IMU660RA 的通信接口对象指针
// 返回参数     imu660ra_state_enum     IMU660RA 状态码 详见 zf_device_imu660ra.h 中 imu660ra_state_enum 定义
// 使用示例     imu660ra_expansion_init(interface);
// 备注信息     本接口为传感器框架接口 并不直接对用户开放
//-------------------------------------------------------------------------------------------------------------------
static imu660ra_state_enum imu660ra_expansion_init(imu_interface_obj_struct *interface) {
    imu660ra_state_enum return_state = IMU660RA_STATE_NO_ERROR;
    do {
        interface->write_8bit_register(IMU660RA_DEV_ADDR, IMU660RA_PWR_CONF, 0x00);  // 关闭高级省电模式
        interface->delay_ms(1);
        interface->write_8bit_register(IMU660RA_DEV_ADDR, IMU660RA_INIT_CTRL, 0x00);  // 开始对模块进行初始化配置
        interface->write_8bit_registers(IMU660RA_DEV_ADDR, IMU660RA_INIT_DATA, imu660ra_config_file,
                                        sizeof(imu660ra_config_file));
        interface->write_8bit_register(IMU660RA_DEV_ADDR, IMU660RA_INIT_CTRL, 0x01);  // 初始化配置结束
        interface->delay_ms(20);

        uint8_t data[2] = {0, 0};
        if (IMU_INTERFACE_SPI == interface->interface_type) {
            interface->read_8bit_registers(IMU660RA_DEV_ADDR, IMU660RA_INT_STA | IMU660RA_SPI_R, data, 2);
        } else {
            data[1] = interface->read_8bit_register(IMU660RA_DEV_ADDR, IMU660RA_INT_STA);
        }
        if (interface->assert(1 == data[1],              // 检查是否配置完成
                              "imu660ra config error.",  // 输出预设报错信息
                              __FILE__, __LINE__)) {
            return_state = IMU660RA_STATE_SET_CONFIG_ERROR;
            break;
        }

        // 开启性能模式  使能陀螺仪、加速度、温度传感器
        interface->write_8bit_register(IMU660RA_DEV_ADDR, IMU660RA_PWR_CTRL, 0x0E);

        if (interface->assert(IMU660RA_ACC_OUTPUT_RATE_64_DIV <= IMU660RA_ACC_OUTPUT_RATE_DEFAULT &&
                                  IMU660RA_ACC_OUTPUT_RATE_32_MUL >= IMU660RA_ACC_OUTPUT_RATE_DEFAULT,
                              "IMU660RA_ACC_SAMPLE_RATE_DEFAULT set error.",  // 输出预设报错信息
                              __FILE__, __LINE__)) {
            return_state = IMU660RA_STATE_SET_ACC_OUTPUT_RATE_ERROR;
            break;
        }
        interface->write_8bit_register(IMU660RA_DEV_ADDR, IMU660RA_ACC_CONF,
                                       0xA0 | (IMU660RA_ACC_OUTPUT_RATE_DEFAULT - 1));

        if (interface->assert(IMU660RA_GYRO_OUTPUT_RATE_2_DIV <= IMU660RA_GYRO_OUTPUT_RATE_DEFAULT &&
                                  IMU660RA_GYRO_OUTPUT_RATE_64_MUL >= IMU660RA_GYRO_OUTPUT_RATE_DEFAULT,
                              "IMU660RA_GYRO_SAMPLE_RATE_DEFAULT set error.",  // 输出预设报错信息
                              __FILE__, __LINE__)) {
            return_state = IMU660RA_STATE_SET_GYRO_OUTPUT_RATE_ERROR;
            break;
        }
        interface->write_8bit_register(IMU660RA_DEV_ADDR, IMU660RA_GYR_CONF,
                                       0xA0 | (IMU660RA_GYRO_OUTPUT_RATE_DEFAULT - 1));

        // IMU660RA_ACC_RANGE 寄存器
        // 设置为 0x00 加速度计量程为 ±2  g
        // 设置为 0x01 加速度计量程为 ±4  g
        // 设置为 0x02 加速度计量程为 ±8  g
        // 设置为 0x03 加速度计量程为 ±16 g
        if (interface->assert(IMU660RA_ACC_RANGE_SGN_2G <= IMU660RA_ACC_RANGE_DEFAULT &&
                                  IMU660RA_ACC_RANGE_SGN_16G >= IMU660RA_ACC_RANGE_DEFAULT,
                              "IMU660RA_ACC_RANGE_DEFAULT set error.",  // 输出预设报错信息
                              __FILE__, __LINE__)) {
            return_state = IMU660RA_STATE_SET_ACC_RANGE_ERROR;
            break;
        }
        interface->write_8bit_register(IMU660RA_DEV_ADDR, IMU660RA_ACC_RANGE, IMU660RA_ACC_RANGE_DEFAULT - 2);

        // IMU660RA_GYR_RANGE 寄存器
        // 设置为 0x04 陀螺仪量程为 ±125  dps
        // 设置为 0x03 陀螺仪量程为 ±250  dps
        // 设置为 0x02 陀螺仪量程为 ±500  dps
        // 设置为 0x01 陀螺仪量程为 ±1000 dps
        // 设置为 0x00 陀螺仪量程为 ±2000 dps
        if (interface->assert(IMU660RA_GYRO_RANGE_SGN_125DPS <= IMU660RA_GYRO_RANGE_DEFAULT &&
                                  IMU660RA_GYRO_RANGE_SGN_2000DPS >= IMU660RA_GYRO_RANGE_DEFAULT,
                              "IMU660RA_GYRO_RANGE_DEFAULT set error.",  // 输出预设报错信息
                              __FILE__, __LINE__)) {
            return_state = IMU660RA_STATE_SET_GYRO_RANGE_ERROR;
            break;
        }
        interface->write_8bit_register(IMU660RA_DEV_ADDR, IMU660RA_GYR_RANGE, 6 - IMU660RA_GYRO_RANGE_DEFAULT);
    } while (0);
    return return_state;
}

